    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
        databaru = false;
        int row = jTable1.getSelectedRow();
        
        String id = jTable1.getModel().getValueAt(row, 0).toString();
        
        Connection conn = koneksi.getKoneksi();
        Statement stm = conn.createStatement();
        
        ResultSet sql = stm.executeQuery("SELECT * FROM karyawan WHERE id_karyawan='" + id + "'");
        
        if (sql.next()) {
            id_karyawan.setText(sql.getString("id_karyawan"));
            nama_karyawan.setText(sql.getString("nama_karyawan"));
            alamat.setText(sql.getString("alamat"));
            tempat_lahir.setText(sql.getString("tempat_lahir"));

            java.util.Date tanggalLahir = sql.getDate("tanggal_lahir");
            if (tanggalLahir != null) {
                tanggal_lahir.setDate(tanggalLahir);
            }

            jenis_kelamin.setText(sql.getString("jenis_kelamin"));
            telepon.setText(sql.getString("telepon"));
            username.setText(sql.getString("username"));
            password.setText(sql.getString("password"));
        }
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(Karyawan.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_jTable1MouseClicked

