    private void getData(){
        try {
            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("SELECT * FROM karyawan");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0); // Reset data tabel

            while (result.next()) {
                String id_karyawan = result.getString("id_karyawan");
                String nama_karyawan = result.getString("nama_karyawan");
                String alamat = result.getString("alamat");
                String tempat_lahir = result.getString("tempat_lahir");
                Date tanggal_lahir = result.getDate("tanggal_lahir");
                String jenis_kelamin = result.getString("jenis_kelamin");
                String telepon = result.getString("telepon");
                String username = result.getString("username");
                String password = result.getString("password");

                model.addRow(new Object[]{id_karyawan, nama_karyawan, alamat, tempat_lahir, tanggal_lahir, jenis_kelamin, telepon, username, password});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Karyawan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
